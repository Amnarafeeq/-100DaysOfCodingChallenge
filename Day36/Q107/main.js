"use strict";
// Write a condition using logical operators that checks if a number is divisible by both 2 and 3.
function conditionUsingLogicalOperator(num) {
    return (num % 2 === 0 && num % 3 === 0);
}
console.log(conditionUsingLogicalOperator(12));
console.log(conditionUsingLogicalOperator(67));
