// Checking if a Value is NaN: Demonstrate how to check if a variable's value is NaN (Not a Number) and return
//  a boolean result.

function checkNotANumber(value:any):boolean{
    return isNaN(value)
}
console.log(checkNotANumber(123456));
console.log(checkNotANumber("Excited to tell that I've completed 30 days of coding challenge"));

