// Converting Strings to Numbers: Create a function that takes a string representing a number (like "123")
//  and converts it into an actual number type.

function stringsToNumbers():void{
    let a:string = "69712"
    console.log(parseInt(a));            //parseInt method convert string whole numbers into actual numbers
    let b:string = "0.545416";
    console.log(parseFloat(b));         // parseFloat method converts string decimal numbers into actual numbers
}
