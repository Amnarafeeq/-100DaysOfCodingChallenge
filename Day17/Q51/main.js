"use strict";
// : Refactoring to Arrow Functions: Take a simple function that calculates the area of a rectangle and refactor
//  it into an arrow function.
let calculateArea = (width, height) => width * height;
console.log(`The area of rectangle is ${calculateArea(10, 6)} square units.`);
console.log(`The area of rectangle is ${calculateArea(27, 18)} square units.`);
