// Determine the Remainder: Make a function that gets two numbers and shows the leftover from dividing them using
//  the % sign. For example, remainder(5, 2) should give 1.

function remainder(num1:number, num2:number){
    return num1 % num2
}
let result:number = remainder(4,3)
console.log(result);

let result2:number = remainder(49,5)
console.log(result2);
